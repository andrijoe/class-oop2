package com.pbo.bean;

public class PerumahanHistori {
	private Long id;
	private String cicilan;
	private String nominal;
	private String denda;
	private String tglcicil;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getCicilan() {
		return cicilan;
	}
	public void setCicilan(String cicilan) {
		this.cicilan = cicilan;
	}
	public String getNominal() {
		return nominal;
	}
	public void setNominal(String nominal) {
		this.nominal = nominal;
	}
	public String getDenda() {
		return denda;
	}
	public void setDenda(String denda) {
		this.denda = denda;
	}
	public String getTglcicil() {
		return tglcicil;
	}
	public void setTglcicil(String tglcicil) {
		this.tglcicil = tglcicil;
	}


}
